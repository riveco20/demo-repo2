package com.ejercicioCadena.EjerciciosConCadenas.EjeciciosNumeros;

public class TipoPrimitivosEnteros {
    public static void main(String[] args) {
        // tipos primitivos Enteros byte , short , int , long
        System.out.println("---Byte---");
        System.out.println("bits tipo byte" + Byte.SIZE);
        System.out.println("Maximo positivo de byte " + Byte.MAX_VALUE);
        System.out.println("Valor minimo de byte" + Byte.MIN_VALUE);

        System.out.println("---Short---");
        System.out.println("bits tipo byte" + Short.SIZE);
        System.out.println("Maximo positivo de byte " + Short.MAX_VALUE);
        System.out.println("Valor minimo de byte" + Short.MIN_VALUE);

        System.out.println("---int---");
        System.out.println("bits tipo byte" + Integer.SIZE);
        System.out.println("Maximo positivo de byte " + Integer.MAX_VALUE);
        System.out.println("Valor minimo de byte" + Integer.MIN_VALUE);

        System.out.println("---Long---");
        System.out.println("bits tipo byte" + Long.SIZE);
        System.out.println("Maximo positivo de byte " + Long.MAX_VALUE);
        System.out.println("Valor minimo de byte" + Long.MIN_VALUE);





    }
}
