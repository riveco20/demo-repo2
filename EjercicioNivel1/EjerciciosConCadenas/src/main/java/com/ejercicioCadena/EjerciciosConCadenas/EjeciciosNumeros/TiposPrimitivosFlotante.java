package com.ejercicioCadena.EjerciciosConCadenas.EjeciciosNumeros;

public class TiposPrimitivosFlotante {
    public static void main(String[] args) {
        //Tipos primitivos Flotante float , double
        System.out.println("---Float---");
        System.out.println("bits tipo byte" + Float.SIZE);
        System.out.println("Maximo positivo de byte " + Float.MAX_VALUE);
        System.out.println("Valor minimo de byte" + Float.MIN_VALUE);

        System.out.println("---Double---");
        System.out.println("bits tipo byte" + Double.SIZE);
        System.out.println("Maximo positivo de byte " + Double.MAX_VALUE);
        System.out.println("Valor minimo de byte" + Double.MIN_VALUE);

        float numeroFloat = 10.5f;
        double doubleNumero = 1.6D;
        float numeroFloatDos = .065F;
    }
}
